					<h2>Which One Doesn't Belong? </h2>
					<p>The idea of comparing groups of items looking at similarities and differences has been explored 
						by many math educators. Christopher Danielson, the author of the blogs Overthinking my Teaching 
						and Talking Math With Your Kids has taken this idea to new heights. Inspired by Sesame Street's 
						"One of these things in not like the others", Danielson wrote the book Which One Doesn't Belong. 
						In this routine students are asked to look at all four images in each of the quadrants and asked
						 to share their reasoning as to "Which One Doesn't Belong and Why?" The great thing about this 
						 routine is that there are no wrong answers, as long as the student's reasoning is true. The 
						 focus is not on the answer, but on the student being able to communicate his/her reasoning/
						 justification of their choice.</p>

					<h2>How do I use it with my students? </h2>
					<p>​I typically begin by asking the students to take some independent time to think about the images. 
						Students need time to process their own ideas before considering others. Sometimes my next step 
						is a pair-share following the thinking time. Then I ask for students to volunteer their ideas.</p>

						<h2>Guiding Questions:</h2>
						<p>What do you notice?</br>
						What makes all the items alike?</br>
						What makes them different?</br>
						Which one doesn’t belong?</br>
						Can you share your reasoning to justify your answer?</p>

						
							<h2>Learning Introduction:</h2>
							<p>1.	Download the what doesn’t belong cards.</br>
								2.	Cut and laminate each of the card.</br>
								3.	Ask your student to identify the picture on each cards.</br> 
								4.	You can play in group and apply “turn taking’.</br>
								5.	In front of student, put the words of the picture.</br>
							6.	Take turn to answer your question or instruction.</br>  
								7.	You can create your instruction according to the situation or the purpose of your lesson.</p>  	
